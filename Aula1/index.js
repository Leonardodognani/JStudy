// Diferente tipos de String com aspas e crase
console.log("Olá, meu nome é Leo");
console.log(`Olá, meu nome é Leo`);
console.log("Olá, meu nome é 'Leo'");
console.log('Olá, meu nome é "Leo"');
console.log(`Olá, meu 'nome' é "Leo"`);

// Exibir números
console.log(16, 123, 1.2);

// Ambos
console.log(18, 9, 5.6, "Leonardo");
